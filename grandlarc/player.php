	static public function death($player, $killer) {
		$player->HasCitySelected = 0;

		if($killer == NULL) { // Equals INVALID_PLAYER_ID
			$player->resetMoney();
			return 1;
		}

		if($player->getMoney() > 0) {
			$killer->giveMoney($player->getMoney());
			$player->resetMoney();
		}

		return 1;
	}

	static public function requestClass($player, $classid) {
		if($player->isNPC())
			return 1;

		if($player->HasCitySelected) {
			static::setupCharSelection($player);
			return 1;
		}

		if($player->getState() != PLAYER_STATE_SPECTATING) {
			$player->toggleSpectating(1);
			Gamemode::$txtClassSelHelp->showForPlayer($player);
			$player->CitySelection = -1;
		}

		return 0;
	}

	static public function update($player) {
		if(!$player->isConnected())
			return 0;
		if($player->isNPC())
			return 1;

		if($player->HasCitySelected != 1 && $player->getState() == PLAYER_STATE_SPECTATING) {
			static::handleCitySelection($player);
			return 1;
		}

		if($player->getInterior() != 0 && $player->getWeapon() != 0) {
			$player->setArmedWeapon(0);
			return 0; // No sync until they change their weapon
		}

		// Don't allow minigun
		if($player->getWeapon() == WEAPON_MINIGUN) {
			$player->kick();
			return 0;
		}
	}

	static public function handleCitySelection($player) {
		$keys = $player->getKeys();
		if($player->CitySelection == -1) {
			static::switchToNextCity($player);
			return;
		}

		if((Server::getTickCount() - $player->LastCitySelectionTick) < 500)
			return;

		if($keys->keys & KEY_FIRE) {
			$player->HasCitySelected = 1;
			Gamemode::$txtLosSantos->hideForPlayer($player);
			Gamemode::$txtSanFierro->hideForPlayer($player);
			Gamemode::$txtLasVenturas->hideForPlayer($player);
			Gamemode::$txtClassSelHelp->hideForPlayer($player);
			$player->toggleSpectating(0);
		}

		if($keys->leftright > 0) {
			static::switchToNextCity($player);
		} else if($keys->leftright < 0) {
			static::switchToPreviousCity($player);
		}
	}

